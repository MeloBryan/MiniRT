#include "miniRT.h"

int	color_to_int(t_color color)
{
	int	r;
	int	g;
	int	b;

	r = (int)(fmin(fmax(color.x, 0.0), 1.0) * 255.0);
	g = (int)(fmin(fmax(color.y, 0.0), 1.0) * 255.0);
	b = (int)(fmin(fmax(color.z, 0.0), 1.0) * 255.0);
	return ((r << 16) | (g << 8) | b);
}
